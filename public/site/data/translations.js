function normalizeLanguage(language) {
  return ['pl', 'en', 'ru'].includes(language) ? language : FALLBACK_LANGUAGE;
}

export function getSiteTranslations(language = FALLBACK_LANGUAGE) {
  return siteTranslations[normalizeLanguage(language)] || siteTranslations[FALLBACK_LANGUAGE];
}

